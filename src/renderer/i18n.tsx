const dictionaries = {
  en: english,
  vi: vietnamese
};

export type I18nKey = keyof Dictionary;

const I18nContext = createContext<Dictionary>(english);

export function I18nProvider({ language, children }: { language: LanguageCode; children: ReactNode }) {
  return (
    <I18nContext.Provider value={dictionaries[language] ?? english}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n(): Dictionary {
  return useContext(I18nContext);
}

export function getI18n(language: LanguageCode): Dictionary {
  return dictionaries[language] ?? english;
}

export function labelForMode(mode: RecordingMode, text: Dictionary): string {
  switch (mode) {
    case "fullscreen":
      return text.fullScreen;
    case "area":
      return text.area;
    case "window":
      return text.window;
    case "browser-tab":
      return text.browserTabMode;
    case "webcam":
      return text.webcam;
    default:
      return text.recordingTitle;
  }
}

export function labelForTool(tool: ToolName, text: Dictionary): string {
  const map: Record<ToolName, I18nKey> = {
    select: "toolSelect",
    pen: "toolPen",
    highlighter: "toolHighlighter",
    text: "toolText",
    arrow: "toolArrow",
    line: "toolLine",
    rectangle: "toolRectangle",
    circle: "toolCircle",
    marker: "toolMarker",
    blur: "toolBlur",
    pixelate: "toolPixelate",
    eraser: "toolEraser"
  };
  return text[map[tool]];
}
